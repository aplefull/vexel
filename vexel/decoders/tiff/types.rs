impl TryFrom<u16> for Compression {
    type Error = VexelError;

    fn try_from(value: u16) -> Result<Self, Self::Error> {
        match value {
            1 => Ok(Self::None),
            2 => Ok(Self::CCITTRLE),
            3 => Ok(Self::CCITTFax3),
            4 => Ok(Self::CCITTFax4),
            5 => Ok(Self::LZW),
            6 => Ok(Self::OldJPEG),
            7 => Ok(Self::JPEG),
            8 => Ok(Self::AdobeDeflate),
            9 => Ok(Self::T85JBIG),
            10 => Ok(Self::T43JBIG),
            32766 => Ok(Self::NeXT),
            32771 => Ok(Self::CCITTRLEWord),
            32773 => Ok(Self::PackBits),
            32809 => Ok(Self::ThunderScan),
            32895 => Ok(Self::IT8CTPAD),
            32896 => Ok(Self::IT8LW),
            32897 => Ok(Self::IT8MP),
            32898 => Ok(Self::IT8BL),
            32908 => Ok(Self::PixarFilm),
            32909 => Ok(Self::PixarLog),
            32946 => Ok(Self::Deflate),
            32947 => Ok(Self::DCS),
            34661 => Ok(Self::JBIG),
            34676 => Ok(Self::SGILog),
            34677 => Ok(Self::SGILog24),
            34712 => Ok(Self::JPEG2000),
            34887 => Ok(Self::LERC),
            34925 => Ok(Self::LZMA),
            50000 => Ok(Self::ZSTD),
            50001 => Ok(Self::WebP),
            50002 => Ok(Self::JXL),
            34933 => Ok(Self::PNG),
            _ => Err(VexelError::Custom(format!("Invalid compression value: {}", value))),
        }
    }
}

#[derive(Debug, Clone, Copy, PartialEq)]
pub enum SampleFormat {
    UnsignedInt = 1,
    SignedInt = 2,
    Float = 3,
    Void = 4,
}

impl Default for SampleFormat {
    fn default() -> Self {
        Self::UnsignedInt
    }
}

impl TryFrom<u32> for SampleFormat {
    type Error = VexelError;

    fn try_from(value: u32) -> Result<Self, Self::Error> {
        match value {
            1 => Ok(Self::UnsignedInt),
            2 => Ok(Self::SignedInt),
            3 => Ok(Self::Float),
            4 => Ok(Self::Void),
            _ => Err(VexelError::Custom(format!("Invalid sample format value: {}", value))),
        }
    }
}

